      <ul>
        <li>Recording teaching time (except during videos)</li>
        <li>Burning a CD</li>
      </ul>
      <h3>Service</h3>
      <ul>
        <li>Making sure preacher has a wireless microphone and the microphone has a good battery</li>
        <li>Managing mixer during service</li>
        <li>Recording special events</li>
        <ul>
          <li>Portrait of Grace</li>
          <li>Dedications and Infant Baptisms</li>
          <li>Etc.</li>
        </ul>
        <li>Recording the Scripture Reading, Sermon and Benediction</li>
        <li>Burning a sermon CD</li>
      </ul>
      <h3>Tear-down</h3>
      <ul>
        <li>Detach snake, shutdown and secure mixer board</li>
        <li>Wrap up snake</li>
        <li>Help others to tear-down sound equipment</li>
        <li>Deliver both CDs to post-processor (usually Kevin)</li>
      </ul>
    </ul>
    <h2>Keep in mind:</h2>
    <ul>
      <li>You are free to switch weeks find substitutes, etc. Please inform the leader of all schedule changes.</li>
