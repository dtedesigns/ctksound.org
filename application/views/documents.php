          <li>The Sound Team Leader (if unavailable, please leave a message)</li>
          <li>Another Sound Team member</li>
          <li>Bill or Megan Gabriel</li>
          <li>The deacon in charge of setup</li>
        </ul>
        <li>Inform Leader of any equipment problems or potential problems.</li>
        <li>Your leader is there to help you serve, make use of him.</li>
        <li>There are equipment manuals available online, spend the time to learn the equipment.</li>
